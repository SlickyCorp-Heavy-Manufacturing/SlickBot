class Grid:
    """16x16 pixel grid for building 8x16-mode metasprites."""
    def __init__(self):
        self.g = [[0] * 16 for _ in range(16)]

    def px(self, x, y, c):
        if 0 <= x < 16 and 0 <= y < 16:
            self.g[y][x] = c

    def rect(self, x, y, w, h, c):
        for yy in range(y, y + h):
            for xx in range(x, x + w):
                self.px(xx, yy, c)

    def rows(self, y, patterns, mapping):
        """patterns: list of 16-char strings; mapping: char->colour."""
        for dy, pat in enumerate(patterns):
            for x, ch in enumerate(pat):
                if ch in mapping:
                    self.px(x, y + dy, mapping[ch])


def meta16(grid):
    """Return [LT, LB, RT, RB] tiles in column-major (8x16) order."""
    def sub(x0, y0):
        rows = []
        for yy in range(y0, y0 + 8):
            rows.append(''.join(str(grid.g[yy][xx]) for xx in range(x0, x0 + 8)))
        return tile_from_rows(rows)
    return [sub(0, 0), sub(0, 8), sub(8, 0), sub(8, 8)]


