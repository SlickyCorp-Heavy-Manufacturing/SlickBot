    bg = build_background_table()
    spr = build_sprite_table()
    data = b''.join(bg) + b''.join(spr)
    assert len(data) == 0x2000, len(data)
    with open(out, 'wb') as fh:
        fh.write(data)
    print(f'wrote {out} ({len(data)} bytes)')


