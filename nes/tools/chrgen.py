"""Generate an 8KB CHR ROM for the Border Run NES game.

Pattern table 0 (0x0000-0x0FFF) = BACKGROUND tiles.
    Font glyphs are placed at their ASCII code (0x20-0x5F) so that a string
    of ASCII bytes doubles as a list of tile indices.  A handful of scenery
    tiles live in the 0x00-0x1F control range and 0x60+ range.

Pattern table 1 (0x1000-0x1FFF) = SPRITE tiles (player, vehicles, pickups).

Each tile is 8x8, 2 bits/pixel, stored in NES planar format:
    bytes 0..7  -> low bit-plane of rows 0..7
    bytes 8..15 -> high bit-plane of rows 0..7
Pixel value 0 = transparent/backdrop, 1..3 = palette colours.
"""

import sys

TILE_BYTES = 16
TABLE_TILES = 256


def tile_from_rows(rows):
    """rows: list of 8 strings, each 8 chars in '0'..'3' (or '.'==0)."""
    assert len(rows) == 8, rows
    plane0 = []
    plane1 = []
    for row in rows:
        assert len(row) == 8, row
        p0 = 0
        p1 = 0
        for col, ch in enumerate(row):
            v = 0 if ch == '.' else int(ch)
            bit = 7 - col
            if v & 1:
                p0 |= (1 << bit)
            if v & 2:
                p1 |= (1 << bit)
        plane0.append(p0)
        plane1.append(p1)
    return bytes(plane0 + plane1)


